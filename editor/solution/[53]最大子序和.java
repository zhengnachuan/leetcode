    class Solution {
        public int maxSubArray(int[] nums) {
            int length = nums.length;
            if (length == 0) {
                return 0;
            }
            // ä»¥ä¸‹æ ‡iç»“å°¾çš„æœ€å¤§å­åºå’Œ
            int[] dp = new int[length];
            dp[0] = nums[0];
            int res = dp[0];
            for (int i = 1; i < length; i++) {
                if (dp[i - 1] >= 0) {
                    dp[i] = nums[i] + dp[i - 1];
                } else {
                    dp[i] = nums[i];
                }
//                System.out.println("dp[" + i + "]=" + dp[i]);
                res = Math.max(res, dp[i]);
            }
            return res;
        }
    }
